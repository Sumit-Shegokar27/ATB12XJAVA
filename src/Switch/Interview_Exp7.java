package Switch;

public class Interview_Exp7 {

    public static void main(String[] args) {

        char ch='C';

        switch(ch){
            default :
                System.out.println("None");
                break;
            case 'A' :
                    System.out.println(" Value of A 65 ");
                    break;
            case 'B' :
                System.out.println(" Value of B  66 ");
                break;
                case 'C' :
            System.out.println("Value of C 67");
            break;

        }
    }
}
