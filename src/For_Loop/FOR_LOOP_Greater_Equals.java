package For_Loop;

public class FOR_LOOP_Greater_Equals {

    public static void main(String[] args) {

        for(int i=0; i <=10; i++){
            System.out.println(i);
        }
        for(int i=1 ; i<=10; i++){
            System.out.println(i);
        }
        for(int i= 0; i< 10; i++){
            System.out.println(i);
        }
    }
}
