package Ternary_Operators;

public class Relational_Operator {

    public static void main(String[] args) {

        int a=50,b=20;
        System.out.println("a==b :" + (a==b));
        System.out.println("a!=b :" + (a!=b));
        System.out.println("a>b  :" + (a>b));
        System.out.println("a<b  :" + (a<b));
        System.out.println("a>=b :" + (a>=b));
        System.out.println("a<=b :" + (a<=b));

    }
}
